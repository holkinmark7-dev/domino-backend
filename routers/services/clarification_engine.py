def match_owner_phrase(text: str) -> List[str]:
    """
    Принимает сырой текст от владельца.
    Возвращает список нормализованных ключей симптомов.
    Поиск нечувствителен к регистру.
    """
    text_lower = text.lower()
    matched = []
    for symptom_key, phrases in OWNER_PHRASES.items():
        for phrase in phrases:
            if phrase in text_lower:
                if symptom_key not in matched:
                    matched.append(symptom_key)
                break
    return matched


def needs_clarification(symptom_key: str) -> Optional[str]:
    """
    Возвращает уточняющий вопрос если симптом требует уточнения.
    Возвращает None если вопрос не нужен.
    """
    if symptom_key in CLARIFICATION_QUESTIONS:
        return CLARIFICATION_QUESTIONS[symptom_key]["question"]
    return None
